def extract_ingredient_key(ing_str):
    """Extract the main ingredient name from an ingredient string like '菠菜 200g'"""
    ing_str = ing_str.strip()
    # Try to match known ingredients (longest first)
    for key in sorted(INGREDIENT_NUTRITION.keys(), key=len, reverse=True):
        if key in ing_str:
            return key
    return None

def generate_highlights(ingredients, dietary):
    highlights = []
    seen = set()
    ingredient_keys = []

    for ing in ingredients:
        key = extract_ingredient_key(ing)
        if key and key not in ingredient_keys:
            ingredient_keys.append(key)

    for key in ingredient_keys:
        for h in INGREDIENT_NUTRITION.get(key, []):
            if h not in seen:
                seen.add(h)
                highlights.append(h)

    for combo_hints, combo_text in COMBINATION_RULES:
        # Check if ALL ingredients in the combo are present
        if all(any(c in k for k in ingredient_keys) or any(c in ing for ing in ingredients) for c in combo_hints):
            # More precise check
            ing_names = " ".join(ingredient_keys) + " " + " ".join(ingredients)
            if all(c in ing_names for c in combo_hints):
                if combo_text not in seen:
                    seen.add(combo_text)
                    highlights.append(combo_text)

    for tag in dietary:
        if tag in DIETARY_HIGHLIGHTS:
            h = DIETARY_HIGHLIGHTS[tag]
            if h not in seen:
                seen.add(h)
                highlights.append(h)

    return highlights[:5]

def main():
    with open("src/data/recipes.json", "r", encoding="utf-8") as f:
        data = json.load(f)

    for recipe in data["recipes"]:
        highlights = generate_highlights(recipe.get("ingredients", []), recipe.get("dietary", []))
        recipe["nutritionHighlights"] = highlights

    with open("src/data/recipes.json", "w", encoding="utf-8") as f:
        json.dump(data, f, ensure_ascii=False, indent=2)

    print(f"Added nutritionHighlights to {len(data['recipes'])} recipes.")

    # Print a few examples
    for r in data["recipes"][:3]:
        print(f"\n{r['title']}:")
        for h in r.get("nutritionHighlights", []):
            print(f"  - {h}")

if __name__ == "__main__":
    main()
